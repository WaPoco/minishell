#include "../../include/libft.h"

size_t	print_num(int nb)
{
	if (nb == -2147483648)
	{
		ft_putstr_fd("-2147483648", 1);
		return (11);
	}
	ft_putnbr_fd(nb, 1);
	return (ft_nbrlen(nb));
}
