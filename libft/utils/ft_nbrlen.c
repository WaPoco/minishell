#include "../../include/libft.h"

size_t	ft_nbrlen(int n)
{
	size_t	len;

	len = 0;
	if (n <= 0)
	{
		len = 1;
		n = -n;
	}
	while (n > 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}
